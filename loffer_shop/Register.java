    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        // TODO add your handling code here:
        String uname=txtuname.getText();
        String email =txtemail.getText();
        String ps=txtps.getText();
        String reps=txtreps.getText();
        if(ps.equals(reps))
        {
            try{
            String q ="INSERT INTO `register` (`user_name`, `email`, `password`, `re_password`) VALUES (?,?,?,?)";
            pst = (PreparedStatement) con.prepareStatement(q);
            pst.setString(1,uname);
            pst.setString(2,email);
            pst.setString(3,ps);
            pst.setString(4,reps);
            pst.execute();
            JOptionPane.showMessageDialog(this, "Register success");
            LogIn lg=new LogIn();
            lg.setVisible(true);
            this.dispose();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,"Please checked your Information.");
        } 
        }
        else{
         JOptionPane.showMessageDialog(this,"Please checked your password again.");
        }
    }//GEN-LAST:event_jLabel4MouseClicked

